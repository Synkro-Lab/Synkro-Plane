# ── Funciones MPU6050 ───────────────────────────────────────────
def leer_raw(reg):
    data = i2c.readfrom_mem(MPU, reg, 2)
    val = (data[0] << 8) | data[1]
    if val > 32767:
        val -= 65536
    return val

def leer_mpu():
    ax = leer_raw(0x3B) / 16384.0
    ay = leer_raw(0x3D) / 16384.0
    az = leer_raw(0x3F) / 16384.0
    gx = leer_raw(0x43) / 131.0
    gy = leer_raw(0x45) / 131.0
    gz = leer_raw(0x47) / 131.0
    temp = leer_raw(0x41) / 340.0 + 36.53
    return ax, ay, az, gx, gy, gz, temp

# ── Loop principal ──────────────────────────────────────────────
while True:
    ax, ay, az, gx, gy, gz, temp = leer_mpu()
    oled.fill(0)
    oled.text("AX:{:.2f} AY:{:.2f}".format(ax, ay), 0, 0)
    oled.text("AZ:{:.2f}".format(az),                0, 12)
    oled.text("GX:{:.2f} GY:{:.2f}".format(gx, gy), 0, 28)
    oled.text("GZ:{:.2f}".format(gz),                0, 40)
    oled.text("TEMP:{:.1f}C".format(temp),           0, 52)
    oled.show()
    time.sleep(0.1)